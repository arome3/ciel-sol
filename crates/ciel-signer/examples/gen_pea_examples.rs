fn bytes32_seq_e001() -> [u8; 32] {
    let mut out = [0u8; 32];
    for i in 0..16 {
        out[i * 2] = 0xe0;
        out[i * 2 + 1] = 0x01 + i as u8;
    }
    out
}

fn bytes32_ee_ab_repeat() -> [u8; 32] {
    // eeeeeeee 0123456789 eeeeeeee 0123456789 eeeeeeee 0123456789 eeeeeeee 01
    // Matches the intent_fingerprint used in warn.json.
    let hex = "eeeeeeee0123456789eeeeeeee0123456789eeeeeeee0123456789eeeeeeee01";
    let mut out = [0u8; 32];
    for (i, b) in out.iter_mut().enumerate() {
        let byte = u8::from_str_radix(&hex[i * 2..i * 2 + 2], 16).unwrap();
        *b = byte;
    }
    out
}

fn bytes32_block_intent_fingerprint() -> [u8; 32] {
    // Matches block.json's intent_fingerprint: abcdef0123456789... repeated
    let hex = "abcdef0123456789abcdef0123456789abcdef0123456789abcdef0123456789";
    let mut out = [0u8; 32];
    for (i, b) in out.iter_mut().enumerate() {
        let byte = u8::from_str_radix(&hex[i * 2..i * 2 + 2], 16).unwrap();
        *b = byte;
    }
    out
}

fn hex_from(bytes: &[u8]) -> String {
    let mut s = String::with_capacity(bytes.len() * 2);
    for b in bytes {
        s.push_str(&format!("{:02x}", b));
    }
    s
}

