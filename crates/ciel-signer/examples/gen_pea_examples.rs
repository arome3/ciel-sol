// gen_pea_examples — Regenerate signed example files for Ciel PEA / 1.0.
//
// USAGE
//   cargo run --package ciel-signer --example gen_pea_examples
//
// WHAT IT DOES
//   1. Derives an Ed25519 test keypair from a hardcoded, publicly-documented
//      32-byte ASCII seed (see TEST_SECRET_SEED below).
//   2. Writes spec/examples/{approve,warn,block,timeout}.json with:
//        - The canonical attestation field values for each verdict class
//        - The test keypair's public key in the `signer` field
//        - A valid Ed25519 signature over the 132-byte Borsh wire encoding
//        - wire.borsh_b64 populated with the base64 of those 132 wire bytes
//   3. Rewrites spec/test-public-key.txt with the real test public key.
//
// IMPORTANT
//   The test secret seed is PUBLIC. The keypair is intentionally committed to
//   the repository so anyone can reproduce the example signatures. DO NOT USE
//   THIS KEYPAIR FOR ANYTHING OTHER THAN SPEC EXAMPLE SIGNATURES. Production
//   issuers MUST generate independent keys.
//
// NOTE ON `block.json` vs the pinned fixture
//   The pinned binary fixture `crates/ciel-signer/fixtures/ciel_attestation_v1.bin`
//   uses a placeholder `signer` value (0x41..0x60) for which no private key
//   exists. That fixture demonstrates *wire format*. The example `block.json`
//   generated by this binary uses the test keypair's real public key so the
//   signature verifies end-to-end. The attestation field values are otherwise
//   identical to the pinned fixture.

use ciel_signer::attestation::{CIEL_ATTESTATION_MAGIC, Verdict, ATTESTATION_VERSION, TIMEOUT_SENTINEL, EXPIRY_SLOTS};
use ciel_signer::pea::{to_pea_json, PeaFlag, PeaFlagSeverity, PeaIntent, PEA_SPEC_VERSION};
use ciel_signer::CielAttestation;
use ed25519_dalek::{Signer, SigningKey};
use std::error::Error;
use std::fs;
use std::path::{Path, PathBuf};

/// Public, documented seed used to derive the spec test keypair.
/// 32 ASCII bytes. Intentionally published; see file-header warning.
const TEST_SECRET_SEED: [u8; 32] = *b"CIEL-PEA-TEST-KEY-V1-DONOTUSE-AB";

/// Canonical example recipe. One per verdict class.
struct Example {
    name: &'static str,
    verdict: Verdict,
    tx_hash: [u8; 32],
    safety_score: u16,
    optimality_score: u16,
    checker_outputs_hash: [u8; 32],
    slot: u64,
    timestamp: i64,
    timeout_at_ms: u16,
    flags: Vec<PeaFlag>,
    intent: Option<PeaIntent>,
    description: &'static str,
}

fn bytes32_from_pattern(start: u8) -> [u8; 32] {
    let mut out = [0u8; 32];
    for (i, b) in out.iter_mut().enumerate() {
        *b = start.wrapping_add(i as u8);
    }
    out
}

fn bytes32_seq_a001() -> [u8; 32] {
    // a001a002..a010 pattern (as used in approve.json)
    let mut out = [0u8; 32];
    for i in 0..16 {
        out[i * 2] = 0xa0;
        out[i * 2 + 1] = 0x01 + i as u8;
    }
    out
}

fn bytes32_seq_b001() -> [u8; 32] {
    let mut out = [0u8; 32];
    for i in 0..16 {
        out[i * 2] = 0xb0;
        out[i * 2 + 1] = 0x01 + i as u8;
    }
    out
}

