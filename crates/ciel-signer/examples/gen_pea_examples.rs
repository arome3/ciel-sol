/// Find the spec/examples directory relative to CARGO_MANIFEST_DIR.
/// CARGO_MANIFEST_DIR = .../ciel/crates/ciel-signer, so spec/ is ../../spec.
fn spec_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("..")
        .join("..")
        .join("spec")
}

fn main() -> Result<(), Box<dyn Error>> {
    // Derive the test keypair deterministically.
    let signing_key = SigningKey::from_bytes(&TEST_SECRET_SEED);
    let verifying_key = signing_key.verifying_key();
    let pubkey_bytes: [u8; 32] = verifying_key.to_bytes();
    let pubkey_hex = hex_from(&pubkey_bytes);

    println!("Ciel PEA / 1.0 — spec example generator");
    println!("========================================");
    println!("Test public key: {}", pubkey_hex);
    println!();

    let examples_dir = spec_dir().join("examples");
    fs::create_dir_all(&examples_dir)?;

    let mut written_paths: Vec<PathBuf> = Vec::new();

    for ex in canonical_examples() {
        // Build the attestation with the real signer pubkey. new() sets
        // magic, version, and expiry_slot = slot + EXPIRY_SLOTS.
        let att = if ex.verdict == Verdict::Timeout {
            // new_timeout() enforces the sentinel scores. We pass the same
            // timeout_at_ms the example expects.
            CielAttestation::new_timeout(
                ex.tx_hash,
                ex.checker_outputs_hash,
                ex.slot,
                pubkey_bytes,
                ex.timestamp,
                ex.timeout_at_ms,
            )
        } else {
            CielAttestation::new(
                ex.tx_hash,
                ex.verdict,
                ex.safety_score,
                ex.optimality_score,
                ex.checker_outputs_hash,
                ex.slot,
                pubkey_bytes,
                ex.timestamp,
                ex.timeout_at_ms,
            )
        };

        // Sanity: magic + version + expiry are derived correctly.
        debug_assert_eq!(att.magic, CIEL_ATTESTATION_MAGIC);
        debug_assert_eq!(att.version, ATTESTATION_VERSION);
        debug_assert_eq!(att.expiry_slot, ex.slot + EXPIRY_SLOTS);

        // Borsh-serialize and sign.
        let wire_bytes = borsh::to_vec(&att)?;
        debug_assert_eq!(wire_bytes.len(), 132, "wire length must be 132");
        let signature = signing_key.sign(&wire_bytes);
        let signature_bytes: [u8; 64] = signature.to_bytes();

        // Build the envelope via the canonical helper.
        let mut envelope =
            to_pea_json(&att, &signature_bytes, &ex.flags, ex.intent.as_ref())?;

        // Attach the per-example description. serde_json::Map is a BTreeMap,
        // so `_description` naturally sorts to the top of the file (underscore
        // precedes letters lexicographically).
        if let Some(map) = envelope.as_object_mut() {
            map.insert(
                "_description".into(),
                serde_json::Value::String(ex.description.to_string()),
            );
        }

        // Pretty-print and write.
        let path = examples_dir.join(format!("{}.json", ex.name));
        fs::write(&path, serde_json::to_string_pretty(&envelope)? + "\n")?;
        println!("wrote {}", path.display());
        written_paths.push(path);
    }

    // Rewrite spec/test-public-key.txt so it always matches the key this
    // binary derives. Keeps the published spec in lockstep with the committed
    // seed and the signed examples.
    let pubkey_txt_path = spec_dir().join("test-public-key.txt");
    let pubkey_txt = format!(
        "Ciel PEA / 1.0 — Test Public Key\n\
         =================================\n\
         \n\
         This Ed25519 public key is used to sign the four example files under\n\
         ./examples/. It is published here so that anyone can verify the example\n\
         signatures without coordinating with the Ciel project.\n\
         \n\
         The key is derived deterministically from the 32-byte ASCII seed defined\n\
         in crates/ciel-signer/examples/gen_pea_examples.rs. The seed is public\n\
         and intentionally committed. Any attacker can trivially impersonate\n\
         signatures under this key. Production issuers MUST derive fresh keys\n\
         and publish their public keys independently.\n\
         \n\
         Public key (32 bytes, hex, lowercase):\n\
         \n\
             {pubkey_hex}\n\
         \n\
         To regenerate example signatures against this key:\n\
         \n\
             cargo run --package ciel-signer --example gen_pea_examples\n\
         \n\
         The binary overwrites spec/examples/*.json and this file with the current\n\
         pubkey derived from the source-committed seed.\n\
         \n\
         DO NOT USE THIS KEYPAIR FOR ANYTHING OTHER THAN REGENERATING SPEC EXAMPLES.\n"
    );
    fs::write(&pubkey_txt_path, pubkey_txt)?;
    println!("wrote {}", pubkey_txt_path.display());

    // Verify every file we wrote roundtrips through the spec verifier logic.
    println!();
    println!("Self-verification pass:");
    for path in &written_paths {
        verify_file(path, &verifying_key)?;
        println!("  ✓ {} signature verified", path.display());
    }

    println!();
    println!("Done. {} example files written and verified.", written_paths.len());
    Ok(())
}

/// Self-verify: decode the envelope we just wrote and verify the signature
/// against the test pubkey. Fails fast if anything drifts.
fn verify_file(path: &Path, verifying_key: &ed25519_dalek::VerifyingKey) -> Result<(), Box<dyn Error>> {
    use base64::{engine::general_purpose::STANDARD as B64, Engine as _};

    let text = fs::read_to_string(path)?;
    let envelope: serde_json::Value = serde_json::from_str(&text)?;

    // Spec version and fixture version checks.
    let spec_version = envelope
        .get("spec_version")
        .and_then(|v| v.as_str())
        .ok_or("missing spec_version")?;
    assert_eq!(spec_version, PEA_SPEC_VERSION);

    // Decode wire bytes.
    let wire_b64 = envelope
        .get("wire")
        .and_then(|v| v.get("borsh_b64"))
        .and_then(|v| v.as_str())
        .ok_or("missing wire.borsh_b64")?;
    let wire_bytes = B64.decode(wire_b64)?;
    assert_eq!(wire_bytes.len(), 132);

    // Decode signature.
    let sig_hex = envelope
        .get("signature")
        .and_then(|v| v.as_str())
        .ok_or("missing signature")?;
    let mut sig_bytes = [0u8; 64];
    for i in 0..64 {
        sig_bytes[i] = u8::from_str_radix(&sig_hex[i * 2..i * 2 + 2], 16)?;
    }
    let signature = ed25519_dalek::Signature::from_bytes(&sig_bytes);

    // Strict Ed25519 verify.
    verifying_key
        .verify_strict(&wire_bytes, &signature)
        .map_err(|e| format!("signature verification failed: {e}"))?;

    Ok(())
}
